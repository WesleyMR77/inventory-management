    private void jBoxProductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBoxProductsActionPerformed
        List<Product> products = new ArrayList<Product>();
        products = productController.actionGetProduct();

        for(int i =0; i < products.size(); i++){
            names.add(products.get(i).getName());
            if(products.get(i).getName().equals(jBoxProducts.getSelectedItem())){
                cont = i;
            }
            products.get(i).getName();
        }
        txtAmount.setText(String.valueOf(products.get(cont).getTheAmount()));
        txtPrice.setText(String.valueOf(products.get(cont).getPrice()));
    }//GEN-LAST:event_jBoxProductsActionPerformed

    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        Product product = new Product();
        ProductController productControll = new ProductController();

        product.setName((String) jBoxProducts.getSelectedItem());
        product.setTheAmount(Integer.parseInt(txtAmount.getText()));
        product.setPrice(Double.parseDouble(txtPrice.getText()));

        productControll.actionUpdateSellProduct(product);
        new FrmMenu().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnBuyActionPerformed

